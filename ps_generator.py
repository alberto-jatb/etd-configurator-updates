# ---------------------------------------------------------------------------

def _verify_block(mode, flows):
    lines = []
    if mode == "Inline":
        if flows.get("inbound"):
            lines += [
                'Write-Host ""',
                'Write-Host "--- INBOUND CONNECTOR ---"',
                "Check-Object $InboundConnectorName { Get-InboundConnector -Identity $InboundConnectorName -ErrorAction Stop }",
                'Write-Host ""',
                'Write-Host "--- TRANSPORT RULES (INCOMING) ---"',
                "Check-Object $BypassRuleName     { Get-TransportRule -Identity $BypassRuleName     -ErrorAction Stop }",
                "Check-Object $QuarantineRuleName { Get-TransportRule -Identity $QuarantineRuleName -ErrorAction Stop }",
                "Check-Object $JunkRuleName       { Get-TransportRule -Identity $JunkRuleName       -ErrorAction Stop }",
            ]
        if flows.get("outbound"):
            lines += [
                'Write-Host ""',
                'Write-Host "--- OUTBOUND CONNECTOR ---"',
                "Check-Object $OutboundConnectorName { Get-OutboundConnector -Identity $OutboundConnectorName -ErrorAction Stop }",
                'Write-Host ""',
                'Write-Host "--- TRANSPORT RULES (OUTGOING) ---"',
                "Check-Object $OutboundTagRuleName { Get-TransportRule -Identity $OutboundTagRuleName -ErrorAction Stop }",
            ]
        if flows.get("internal"):
            lines += [
                'Write-Host ""',
                'Write-Host "--- JOURNAL RULE (INTERNAL) ---"',
                "Check-Object $JournalRuleName { Get-JournalRule -Identity $JournalRuleName -ErrorAction Stop }",
            ]
    else:
        lines += [
            'Write-Host ""',
            'Write-Host "--- JOURNALING NDR ---"',
            "try {",
            "    $tc = Get-TransportConfig -ErrorAction Stop",
            "    if ($tc.JournalingReportNdrTo) {",
            '        Write-Host "Journaling NDR Address : $($tc.JournalingReportNdrTo)" -ForegroundColor Green',
            "    } else {",
            '        Write-Host "Journaling NDR Address : NOT CONFIGURED" -ForegroundColor Yellow',
            "    }",
            "} catch {",
            '    Write-Host "Unable to retrieve Transport Config" -ForegroundColor Red',
            "}",
            'Write-Host ""',
            'Write-Host "--- JOURNAL RULE ---"',
            "Check-Object $JournalRuleName { Get-JournalRule -Identity $JournalRuleName -ErrorAction Stop }",
            'Write-Host ""',
            'Write-Host "--- JOURNALING OUTBOUND CONNECTOR ---"',
            "Check-Object $JournalConnectorName { Get-OutboundConnector -Identity $JournalConnectorName -ErrorAction Stop }",
        ]
    return lines


