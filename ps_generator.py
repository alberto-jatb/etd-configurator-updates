"""
Generates PowerShell scripts for Cisco ETD Exchange Online configuration.
"""


def _ps_array(items):
    """Convert a Python list to a PowerShell array string."""
    quoted = [f'"{item}"' for item in items]
    return "@(" + ", ".join(quoted) + ")"


def _ps_functions():
    return """\
function Check-Object {
    param ([string]$Name, [scriptblock]$Command)
    try {
        & $Command | Out-Null
        Write-Host "$Name : CONFIGURED" -ForegroundColor Green
    } catch {
        Write-Host "$Name : NOT CONFIGURED" -ForegroundColor Yellow
    }
}

function Install-Object {
    param ([string]$Name, [scriptblock]$Command)
    Write-Host "Installing $Name ..." -ForegroundColor Cyan
    try {
        & $Command
        Write-Host "$Name installed successfully." -ForegroundColor Green
    } catch {
        Write-Host "ERROR installing $Name : $($_.Exception.Message)" -ForegroundColor Red
        Disconnect-ExchangeOnline -Confirm:$false
        exit 1
    }
}

function Remove-Object {
    param ([string]$Name, [scriptblock]$Command)
    Write-Host "Removing $Name ..." -ForegroundColor Cyan
    try {
        & $Command
        Write-Host "$Name removed successfully." -ForegroundColor Yellow
    } catch {
        Write-Host "ERROR removing $Name : $($_.Exception.Message)" -ForegroundColor Red
    }
}
"""


def generate_script(config):
    """
    Generate a complete PowerShell script based on configuration.

    config keys:
        admin_upn         : str
        journal_address   : str
        deployment_mode   : "Journaling" | "Inline"
        flows             : dict {inbound, outbound, internal} (Inline only)
        etd_ips           : list[str]  - Cisco ETD IP addresses
        smart_host        : str        - Outbound smart host
        seg_in_front      : bool
        seg_ips           : list[str]
        xpass_value       : str
        geo               : str
        operation         : "verify" | "install" | "remove"
    """
    mode      = config["deployment_mode"]
    admin_upn = config["admin_upn"]
    journal   = config["journal_address"]
    operation = config["operation"]
    flows     = config.get("flows", {})
    etd_ips              = config.get("etd_ips", [])
    smart_host           = config.get("smart_host", "")
    outbound_rule_enabled = config.get("outbound_rule_enabled", True)
    seg_front            = config.get("seg_in_front", False)
    seg_ips   = config.get("seg_ips", [])
    xpass     = config.get("xpass_value", "12345")
    geo       = config.get("geo", "")

    # IPs to use for inbound connector and bypass rule
    source_ips = seg_ips if (seg_front and seg_ips) else etd_ips

    lines = []

    # Header
    lines += [
        "# =========================================================",
        "# Cisco Secure Email Threat Defense - Exchange Online",
        f"# Mode        : {mode}",
        f"# GEO         : {geo}",
        f"# Operation   : {operation.upper()}",
        f"# Admin UPN   : {admin_upn}",
        "# Generated by ETD Configurator",
        "# =========================================================",
        "",
    ]

    # Helper functions
    lines += [_ps_functions()]

    # --- Variable declarations ---
    lines += [f'$AdminUPN          = "{admin_upn}"']
    lines += [f'$JournalAddress    = "{journal}"']
    lines += [f'$JournalNdrAddress = "{admin_upn}"']

    if mode == "Inline":
        if flows.get("inbound"):
            lines += [
                '$InboundConnectorName = "Cisco Secure Email Threat Defense Inbound"',
                '$BypassRuleName       = "Cisco Secure Email Threat Defense Bypass Spam Filter"',
                '$QuarantineRuleName   = "Cisco Secure Email Threat Defense Quarantine Rule"',
                '$JunkRuleName         = "Cisco Secure Email Threat Defense Junk Rule"',
            ]
        if flows.get("outbound"):
            rule_state = "$true" if outbound_rule_enabled else "$false"
            lines += [
                '$OutboundConnectorName  = "Cisco Secure Email Threat Defense Outbound"',
                '$OutboundTagRuleName    = "Cisco Secure Email Threat Defense Outbound Tag"',
                f'$OutboundSmartHost      = "{smart_host}"',
                f'$XPassValue             = "{xpass}"',
                f'$OutboundTagRuleEnabled = {rule_state}',
            ]
        if flows.get("internal"):
            lines += ['$JournalRuleName = "Cisco Secure Email Threat Defense"']
    else:
        lines += [
            '$JournalRuleName          = "Cisco Secure Email Threat Defense"',
            '$JournalConnectorName     = "Cisco ETD Journaling Direct"',
            '$JournalDomain            = ($JournalAddress -split "@")[1]',
        ]

    if source_ips:
        lines += [f"$CiscoIPs = {_ps_array(source_ips)}"]

    lines += [""]

    # --- Connect ---
    lines += [
        'Write-Host ""',
        'Write-Host "Connecting to Exchange Online..." -ForegroundColor Cyan',
        "Connect-ExchangeOnline -UserPrincipalName $AdminUPN -ShowBanner:$false",
        "",
    ]

    # --- Operation body ---
    if operation == "verify":
        lines += _verify_block(mode, flows)
    elif operation == "install":
        lines += _install_block(mode, flows, source_ips)
    elif operation == "remove":
        lines += _remove_block(mode, flows)

    # --- Disconnect ---
    lines += [
        "",
        "Disconnect-ExchangeOnline -Confirm:$false",
        'Write-Host ""',
        'Write-Host "Operation completed." -ForegroundColor Cyan',
    ]

    return "\n".join(lines)


