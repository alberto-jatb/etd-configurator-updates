def _remove_block(mode, flows):
    lines = []
    if mode == "Inline":
        if flows.get("inbound"):
            lines += [
                "Remove-Object $InboundConnectorName  { Get-InboundConnector -Identity $InboundConnectorName -ErrorAction Stop | Remove-InboundConnector -Confirm:$false }",
                "Remove-Object $BypassRuleName        { Get-TransportRule -Identity $BypassRuleName     -ErrorAction Stop | Remove-TransportRule -Confirm:$false }",
                "Remove-Object $QuarantineRuleName    { Get-TransportRule -Identity $QuarantineRuleName -ErrorAction Stop | Remove-TransportRule -Confirm:$false }",
                "Remove-Object $JunkRuleName          { Get-TransportRule -Identity $JunkRuleName       -ErrorAction Stop | Remove-TransportRule -Confirm:$false }",
            ]
        if flows.get("outbound"):
            lines += [
                "Remove-Object $OutboundConnectorName { Get-OutboundConnector -Identity $OutboundConnectorName -ErrorAction Stop | Remove-OutboundConnector -Confirm:$false }",
                "Remove-Object $OutboundTagRuleName   { Get-TransportRule -Identity $OutboundTagRuleName -ErrorAction Stop | Remove-TransportRule -Confirm:$false }",
            ]
        if flows.get("internal"):
            lines += [
                "Remove-Object $JournalRuleName { Get-JournalRule -Identity $JournalRuleName -ErrorAction Stop | Remove-JournalRule -Confirm:$false }",
            ]
    else:
        lines += [
            "Remove-Object $JournalRuleName     { Get-JournalRule -Identity $JournalRuleName -ErrorAction Stop | Remove-JournalRule -Confirm:$false }",
            "Remove-Object $JournalConnectorName { Get-OutboundConnector -Identity $JournalConnectorName -ErrorAction Stop | Remove-OutboundConnector -Confirm:$false }",
        ]
    return lines


